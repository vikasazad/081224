export const getCategoryData = (categoryId: string): RoomCategoryData | null => {
  const categoryInfo = categoryInfoMap[categoryId];
  const rooms = roomsByCategory[categoryId];

  if (!categoryInfo || !rooms) {
    return null;
  }

  return {
    categoryInfo,
    rooms,
  };
};

export const getAllCategories = (): CategoryInfo[] => {
  return Object.values(categoryInfoMap);
};

