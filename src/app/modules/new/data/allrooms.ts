export const getCategoryById = (
  categoryId: string,
): RoomCategory | undefined => {
  return categories.find((cat) => cat.id === categoryId);
};

export const getRoomsByCategory = (categoryId: string): RoomListItem[] => {
  if (categoryId === "all") return rooms;
  return rooms.filter((room) => room.categoryId === categoryId);
};

export const getRoomsByStatus = (status: string): RoomListItem[] => {
  if (status === "all") return rooms;
  return rooms.filter((room) => room.status === status);
};

